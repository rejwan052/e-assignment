package com.eassignment.web.error;

public class AssignmentDateTimeException extends RuntimeException{

	private static final long serialVersionUID = 17157920836594243L;

	public AssignmentDateTimeException() {
		super();
	}

	public AssignmentDateTimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public AssignmentDateTimeException(String message) {
		super(message);
	}

	public AssignmentDateTimeException(Throwable cause) {
		super(cause);
	}
	
	

}
